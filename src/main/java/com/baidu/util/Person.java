package com.baidu.util;

public class Person {

	private String name;
	
	private String ll;
	
	private String pwd;
	
	private String dd;
	

	public String getPwd() {
		return pwd;
	}

	public void setPwd(String pwd) {
		this.pwd = pwd;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}

	@Override
	public String toString() {
		return "Person [name=" + name + ", ll=" + ll + ", pwd=" + pwd + ", dd="
				+ dd + "]";
	}
	
	//nihaokakak
	
	
}
